package io.openim.flutter_openim_sdk_ffi.models

class PictureInfo {
    /**
     * 唯一ID
     */
    var uuid: String? = null

    /**
     * 图片类型
     */
    var type: String? = null

    /**
     * 图片大小
     */
    var size: Long = 0

    /**
     * 图片宽度
     */
    var width = 0

    /**
     * 图片高度
     */
    var height = 0

    /**
     * 图片oss地址
     */
    var url: String? = null
}